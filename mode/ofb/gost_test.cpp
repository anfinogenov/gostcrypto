void print_128bit_BE (uint8_t* vec)
{
    for (int i = 15; i >= 0; i--)
    {
        std::cout << std::hex << std::uppercase << std::setfill('0') << std::setw(2) << (int)vec[i] << " ";
    }
    std::cout << std::endl;
}

